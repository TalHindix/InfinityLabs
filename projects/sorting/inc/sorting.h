#include <stddef.h> /* size_t */

/* BubbleSort: Sorts an array of integers in ascending order using bubble sort.
 * Parameters:
 *   arr  - pointer to the array
 *   size - number of elements in the array
 */
void BubbleSort(int* arr, size_t size);

/* SelectionSort: Sorts an array of integers in ascending order using selection sort.
 * Parameters:
 *   arr  - pointer to the array
 *   size - number of elements in the array
 */
void SelectionSort(int* arr, size_t size);

/* InsertionSort: Sorts an array of integers in ascending order using insertion sort.
 * Parameters:
 *   arr  - pointer to the array
 *   size - number of elements in the array
 */
void InsertionSort(int* arr, size_t size);


